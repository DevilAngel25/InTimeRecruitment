    public class EmailService : IIdentityMessageService
    {
        public Task SendAsync(IdentityMessage message)
        {
            string GmailUsername = ConfigurationManager.AppSettings["GmailUserNameKey"];
            string GmailPassword = ConfigurationManager.AppSettings["GmailPasswordKey"];
            string GmailHost = ConfigurationManager.AppSettings["GmailHostKey"];
            int GmailPort = Int32.Parse(ConfigurationManager.AppSettings["GmailPortKey"]);// Gmail can use ports 25, 465 & 587; but must be 25 for medium trust environment.
            bool GmailSSL = Boolean.Parse(ConfigurationManager.AppSettings["GmailSSLKey"]);
            bool IsHtml = true;

            SmtpClient smtp = new SmtpClient();
            smtp.Host = GmailHost;
            smtp.Port = GmailPort;
            smtp.EnableSsl = GmailSSL;
            smtp.DeliveryMethod = SmtpDeliveryMethod.Network;
            smtp.EnableSsl = true;
            smtp.UseDefaultCredentials = false;
            smtp.Credentials = new NetworkCredential(GmailUsername, GmailPassword);
            smtp.Timeout = 20000;

            /*
            System.Net.Mail.Attachment attachment;
            attachment = new System.Net.Mail.Attachment("c:/textfile.txt");
            mail.Attachments.Add(attachment);
            */

            MailMessage email = new MailMessage(GmailUsername, message.Destination);
            
            email.Subject = message.Subject;
            email.Body = message.Body;
            email.IsBodyHtml = IsHtml;

            return smtp.SendMailAsync(email);
            
        }
    }

